            হওয়ার পর নির্ধারিত ফি ( মেয়াদোত্তীর্ণের ১৫ দিনের মধ্যে হলে ১৩২৩/- টাকা ও মেয়াদোত্তীর্ণের ১৫ দিন পরে প্রতি
            বছর ২০০/- টাকা জরিমানাসহ ) জমা দিয়ে প্রয়োজনীয় কাগজপত্রসহ বিআরটিএর নির্দিষ্ট সার্কেল অফিসে আবেদন করতে হবে।
