            <br>
            ৩। ন্যাশনাল আইডি কার্ড / জন্ম সনদ/পাসপোর্ট এর সত্যায়িত ফটোকপি।
            <br>

            ৪। নির্ধারিত ফী, ১ ক্যাটাগরি-৩৪৫/-টাকা ও ২ ক্যাটাগরি-৫১৮/-টাকা বিআরটিএ’র নির্ধারিত ব্যাংকে (ব্যাংক এর তালিকা
            www.brta.gov.bd –তে পাওয়া যাবে) জমাদানের রশিদ।।
