            <br>
            লিখিত, মৌখিক ও ফিল্ড টেস্ট পরীক্ষায় উত্তীর্ণ হওয়ার পর পুনরায় একটি নির্ধারিত ফরমে প্রয়োজনীয় কাগজপত্র ও ফী
            প্রদান করে স্মার্টকার্ড ড্রাইভিং লাইসেন্স-এর জন্য সংশিস্নষ্ট সার্কেল অফিসে আবেদন করতে হবে। গ্রাহকের
            বায়োমেট্রিক্স (ডিজিটাল ছবি, ডিজিটাল স্বাক্ষর ও আঙ্গুলের ছাপ) গ্রহণপূর্বক স্মার্ট কার্ড ইস্যু করা হয়। স্মার্ট
            কার্ড ড্রাইভিং লাইসেন্স প্রন্টিং সম্পন্ন হলে গ্রাহককে এসএমএস এর মাধ্যমে তা গ্রহণের বিষয়টি জানিয়ে দেয়া হয়।
