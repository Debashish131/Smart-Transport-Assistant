            <h3 class="justify-content-center" style="color: #d39e00"> ড্রাইভিং লাইসেন্স প্রাপ্তির প্রক্রিয়া</h3>
            <span
                class="justify-content-center">লাইসেন্সের পূর্বশর্ত হলো লার্নার বা শিক্ষানবিশ ড্রাইভিং লাইসেন্স।</span>
            <br>
            গ্রাহককে প্রথমে লার্নার বা শিক্ষানবিশ ড্রাইভিং লাইসেন্স-এর জন্য প্রয়োজনীয় কাগজপত্রসহ আবেদন করতে হবে।
            গ্রাহককে তার স্থায়ী ঠিকানা বা বর্তমান ঠিকানা (প্রয়োজনীয় প্রমাণাদিসহ) বিআরটিএ’র যে সার্কেলের আওতাভূক্ত তাকে
            সেই সার্কেল অফিসে আবেদন করতে হবে। সার্কেল অফিস কর্তৃপক্ষ তাকে একটি শিক্ষানবিস বা লার্নার ড্রাইভিং লাইসেন্স
            প্রদান করবে যা দিয়ে আবেদনকারী ড্রাইভিং প্রশিক্ষণ গ্রহণ করতে পারবে। ২/৩ মাস প্রশিক্ষণ গ্রহণের পর তাকে
            নির্ধারিত তারিখ ও সময়ে নির্ধারিত কেন্দ্রে লিখিত, মৌখিক ও ফিল্ড টেস্ট-এ অংশ গ্রহণ করতে হবে। এসময় প্রার্থীকে
            তার লার্নার বা শিক্ষানবিশ ড্রাইভিং লাইসেন্স (মূল কপি) ও লিখিত পরীক্ষায় অংশগ্রহণের জন্য কলম সাথে আনতে হবে।
            পেশাদার ড্রাইভিং লাইসেন্স-এর জন্য বয়স ন্যূনতম ২০ বছর এবং অপেশাদার এর জন্য ন্যূনতম ১৮ বছর হতে হবে।
            লার্নার বা শিক্ষানবিশ ড্রাইভিং লাইসেন্স এর জন্য প্রয়োজনীয় কাগজপত্র:
