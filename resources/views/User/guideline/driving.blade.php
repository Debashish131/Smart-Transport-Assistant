            প্রতি বছর ২০০/- টাকা জরিমানাসহ) জমা দিয়ে প্রয়োজনীয় কাগজপত্রসহ বিআরটিএর নির্দিষ্ট সার্কেল অফিসে আবেদন করতে
            হবে। আবেদনপত্র ও সংযুক্ত কাগজপত্র সঠিক পাওয়া গেলে একইদিনে গ্রাহকের বায়োমেট্রিক্স (ডিজিটাল ছবি, ডিজিটাল
            স্বাক্ষর ও আঙ্গুলের ছাপ) গ্রহণ করা হয়। স্মার্ট কার্ড wপ্রন্টিং সম্পন্ন হলে গ্রাহককে এসএমএস এর মাধ্যমে জানিয়ে
            দেয়া হয়।
